(
	Id_z int primary key AUTO_INCREMENT,
	Id_ter int not null,
	foreign key (Id_ter) references Terminarz (Id_ter),
	Id_s int not null,
	foreign key (Id_s) references Skoczki (Id_s),
	Wynik1 float not null,
	Pkt_1 float not null,
	Wynik2 float,
	Pkt_2 float
);

